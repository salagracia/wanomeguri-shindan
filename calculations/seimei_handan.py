# ============= 三才配置（陰陽五行） =============
def gogyou(num: int) -> str:
    """画数から五行を判定"""
    last = num % 10
    if last in (1, 2):
        return "木"
    elif last in (3, 4):
        return "火"
    elif last in (5, 6):
        return "土"
    elif last in (7, 8):
        return "金"
    else:
        return "水"


