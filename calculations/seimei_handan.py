def char_kakusu(c: str) -> int:
    """1文字の画数を取得"""
    if c in KANJI_KAKUSU:
        return KANJI_KAKUSU[c]
    if c in KANA_KAKUSU:
        return KANA_KAKUSU[c]
    # 未登録：平均8画と仮定（暫定）
    return 8


def word_kakusu(word: str) -> int:
    """文字列の合計画数"""
    return sum(char_kakusu(c) for c in word if c.strip())


