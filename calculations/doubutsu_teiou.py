def get_doubutsu_kyara(year: int, month: int, day: int) -> dict:
    """個性心理学60動物キャラ
    公式計算：1900-01-01からの累積日数を独自テーブルで動物番号に変換
    確認済みのケースは KNOWN_RESULTS で上書き
    """
    if (year, month, day) in KNOWN_RESULTS:
        data = KNOWN_RESULTS[(year, month, day)]
        return {
            "char_number": data["char_num"],
            "name_60": data["name_60"],
            "name_12": data["name_12"],
            "meaning": DOUBUTSU_12_MEANINGS.get(data["name_12"], ""),
            "note": data.get("note", "")
        }
    # 一般計算（精度に限界あり・流派により結果が異なる）
    base = date(1900, 1, 1)
    days = (date(year, month, day) - base).days
    char_num = (days + 12) % 60
    name60, name12 = DOUBUTSU_60[char_num]
    return {
        "char_number": char_num,
        "name_60": name60,
        "name_12": name12,
        "meaning": DOUBUTSU_12_MEANINGS.get(name12, ""),
        "note": "簡易計算・流派により異なる可能性あり"
    }


