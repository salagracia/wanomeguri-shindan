def get_shusei(year: int, month: int, day: int) -> dict:
    """算命学の主星（中央星）：日干×月支早見表（高尾義政流）"""
    solar = Solar.fromYmd(year, month, day)
    lunar = solar.getLunar()
    day_kan = lunar.getDayGan()
    month_shi = lunar.getMonthZhi()
    shusei = SHUSEI_TABLE.get((day_kan, month_shi), "石門星")
    return {
        "name": shusei,
        "day_kan": day_kan,
        "month_shi": month_shi,
        "meaning": SHUSEI_MEANINGS.get(shusei, "")
    }


